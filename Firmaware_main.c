/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

  uint32_t adc_val;
  float voltage, temperature;
  float prev_temperature = 0;
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	    /* ---------- Read ADC ---------- */
	    HAL_ADC_Start(&hadc1);
	    HAL_Delay(5);
	    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
	    adc_val = HAL_ADC_GetValue(&hadc1);
	    voltage = (adc_val * 3.3f) / 4095.0f;
	    temperature = voltage * 100.0f;

	    // Check temperature change (>1°C)
	    if ((temperature - prev_temperature > 0.1f) ||
	        (prev_temperature - temperature > 0.1f))
	    {
	    	printf("Temp: %.2f | ADC: %lu | Volt: %.2f | Err: %.2f | PWM: %.0f\r\n",
	    	       temperature,adc_val,voltage,setpoint - temperature, pwm_output);
	    }

	    /* ---------- PID Control ---------- */
	    float error = setpoint - temperature;
	    integral += error;
	    float derivative = error - prev_error;

	    pwm_output = Kp * error + Ki * integral + Kd * derivative;

	    // Clamp PID output
	    if (pwm_output > pwm_max) pwm_output = pwm_max;
	    if (pwm_output < 0) pwm_output = 0;

	    // Turn OFF all LEDs // For Continous LED Glow
	    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14, GPIO_PIN_RESET);

	    /* ---------- Apply discrete PWM based on temperature zones ---------- */
	    if (temperature < 26)
	    {
	        pwm_output = pwm_low;  // Low speed
	        HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);   // Green LED
	        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13 | GPIO_PIN_14, GPIO_PIN_RESET);
	    }
	    else if (temperature >= 27 && temperature <= 28)
	    {
	        pwm_output = pwm_mid;  // Medium speed
	        HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);   // Yellow LED
	        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_14, GPIO_PIN_RESET);
	    }
	    else // temperature > temp_mid
	    {
	        pwm_output = pwm_high;  // High speed
	        HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);   // Red LED
	        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13, GPIO_PIN_RESET);
	    }

	    prev_temperature = temperature;

	    /* ---------- Update PWM duty cycle ---------- */
	    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, (uint32_t)pwm_output);

	    prev_error = error;

	    HAL_Delay(1000);
}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

  /* USER CODE END 3 */
}

